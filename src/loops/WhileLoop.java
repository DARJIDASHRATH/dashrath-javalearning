package loops;

/**
 * using while loop we print 1 to 10 number
 * 
 * @author DASHRATH
 * @version 4.1
 * @since
 */

public class WhileLoop {

	public static void main(String[] args) {
		int i = 1;
		int n = 10;
		while (i <= n) // while loop conditions
		{
			System.out.println(i);
			i++; // increment i on every loop
		}

	}

}