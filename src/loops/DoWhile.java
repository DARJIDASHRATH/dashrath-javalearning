package loops;

/**
 * Using do-while loop print 1 to 10
 * 
 * 
 * @author DASHRATH
 * @version 4.2
 * @since
 */

public class DoWhile {

	public static void main(String[] args) {
		int i = 1;
		do {
			System.out.println(i);
			i++; // Increament in i veriable
		} while (i <= 10); // While loop contion
	}

}
